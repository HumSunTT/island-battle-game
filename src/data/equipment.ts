export function getRandomIslandImage(usedImages: string[]): string {
  const available = ISLAND_IMAGES.filter(img => !usedImages.includes(img));
  if (available.length === 0) {
    return ISLAND_IMAGES[Math.floor(Math.random() * ISLAND_IMAGES.length)];
  }
  return available[Math.floor(Math.random() * available.length)];
}

export function getIslandNameFromImage(imageName: string): string {
  return imageName.replace('.png', '');
}

export function getPlayerBaseImage(playerIndex: number): string {
  return PLAYER_BASE_IMAGES[playerIndex % PLAYER_BASE_IMAGES.length];
}

